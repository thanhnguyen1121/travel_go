abstract class DiscoverModel {
  int id;

  String firstCharacter;

  String firstParagraph;

  String secondParagraph;

  String imageOne;

  String title;

  String location;

  String fourParagraph;

  String fiveParagraph;

  List<String> highlightImage;

  String sixParagraph;

  DiscoverModel({
    required this.id,
    required this.firstCharacter,
    required this.firstParagraph,
    required this.secondParagraph,
    required this.imageOne,
    required this.title,
    required this.location,
    required this.fourParagraph,
    required this.fiveParagraph,
    required this.highlightImage,
    required this.sixParagraph,
  });
}
